def translate(key, upperCase=False):
    global translation

    translations = translation.get(key)
    if translations is None:
        raise KeyError(key)
    translated = translations.get(get_addon_preferences().language)
    if translated is not None:
        if upperCase:
            return translated.upper()
        return translated

    if upperCase:
        return translations.get('ENGLISH').upper()

    return translations.get('ENGLISH')
